// Celsius to Fahrenheit 
let celsius = 22;
let fahrenheit = 22* 1.8 +32;
console.log(celsius + "°C is equal to " + fahrenheit.toFixed(2) + "°F");

// Celsius to Kelvin 
let kelvin = celsius + 273.15;
console.log(celsius + "°C is equal to " + kelvin.toFixed(2) + "°K");

// Fahrenheit to Celsius 
let Fahrenheit = 84;
let fahrenheitToCelsius = (Fahrenheit - 32) / 1.8;
console.log(fahrenheit + "°F is equal to "+ fahrenheitToCelsius.toFixed(2) + "°C");



